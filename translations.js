// Language selection and translation functions
function getSelectedLanguage() {
  return localStorage.getItem('selectedLanguage') || 'en';
}

function setLanguage(lang) {
  localStorage.setItem('selectedLanguage', lang);
  applyTranslations();
}

function applyTranslations() {
  const lang = getSelectedLanguage();
  const t = translations[lang] || translations.en;
  const fallback = translations.en; // Fallback to English
  
  // Get all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    const translation = t[key] || fallback[key] || ''; // Use translation or fallback to English
    
    if (translation) {
      if (element.tagName === 'INPUT' && element.type !== 'submit' && element.type !== 'password') {
        element.placeholder = translation;
      } else if (element.tagName === 'INPUT' && element.type === 'password') {
        element.placeholder = translation;
      } else {
        // Preserve HTML structure for elements with children
        if (element.children.length > 0) {
          // For elements with children, update only text nodes
          Array.from(element.childNodes).forEach(node => {
            if (node.nodeType === 3) { // Text node
              node.textContent = translation;
            }
          });
        } else {
          element.textContent = translation;
        }
      }
    }
  });
  
  // Update page title
  if (t.pageTitle) {
    document.title = t.pageTitle;
  }
}

// Initialize translations on page load
if (typeof document !== 'undefined') {
  document.addEventListener('DOMContentLoaded', applyTranslations);
}

